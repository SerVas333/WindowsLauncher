namespace WindowsLauncher.Core.Enums
{
    /// <summary>
    /// Стратегии очистки персональных данных WebView2
    /// </summary>
    public enum DataClearingStrategy
    {
        /// <summary>
        /// Немедленная очистка при закрытии каждого окна
        /// Максимальная безопасность, но может влиять на производительность
        /// </summary>
        Immediate = 0,

        /// <summary>
        /// Очистка при смене пользователя или logout
        /// Оптимальный баланс безопасности и производительности (по умолчанию)
        /// </summary>
        OnUserSwitch = 1,

        /// <summary>
        /// Очистка только при выходе из приложения
        /// Максимальная производительность, минимальная безопасность
        /// </summary>
        OnAppExit = 2
    }
}